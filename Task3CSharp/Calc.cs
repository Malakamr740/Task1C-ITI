
namespace Task3CSharp
{
    public class Calc
    {

        public static long sum(int num1 , int num2)
        {
            return num1 + num2;
        }
        public static long sub(int num1 , int num2)
        {
            return num1 - num2;
        }
        public static long mul(int num1 , int num2)
        {
            return num1 * num2;
        }
        public static long div(int num1 , int num2)
        {
            return num1 / num2;
        }


    }
}
